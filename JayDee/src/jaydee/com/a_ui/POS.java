    public void addItem(Engine_POS eng){
        this.pos_eng = eng;
        index = tblmodel.getRowCount();
        if(end_of_init && pos_eng != null){
        if (tblmodel.getRowCount() <= 0) {
            index = 1;
        } else {
            index = tblmodel.getRowCount() + 1;
        }
        int c = 0; int k = 1;
                    for(int r = 0; r < tblmodel.getRowCount(); r++){
                        tblmodel.setValueAt(k, r, c);
                        k++;
                    }

        String[] data = {k + "", eng.drug.name, eng.drug.code, eng.qty +"", eng.drug.price+""};
               
        tblmodel.addRow(data);
        
        computeSum(eng.qty * eng.drug.price);
        
        }
    }
    
