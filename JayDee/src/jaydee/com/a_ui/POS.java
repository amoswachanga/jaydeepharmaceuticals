    public void clearFields(){
        txtItemAdd.setText("");
        txtSelectedItem.setText("");
        for(int i = 0; i <= tblStudents.getRowCount(); i++){
            tblmodel.removeRow(i);
        }
            tblmodel.fireTableDataChanged();
            for(int i = 0; i < pos.size(); i++){
                pos.remove(i);
            }
            lblTotal.setText("");
            sum = 0.0;
    }
    
