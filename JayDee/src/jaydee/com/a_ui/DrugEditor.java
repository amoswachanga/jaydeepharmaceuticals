    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Are you sure you want to delete the drug:'" + druglist.get(index).name + "?'", "Confirm Delete", JOptionPane.INFORMATION_MESSAGE) == JOptionPane.YES_OPTION){
            try {
                String query = "DELETE FROM drugs WHERE did = "+druglist.get(index).getDid()+";";
                db.stmt.executeUpdate(query);
                lbl_info.setText("Deletion success!");
                initDrugsList();
            } catch (SQLException ex) {
                lbl_info.setText("Unable to delete, sorry!Error DELETE_ERR_DRUGEDITOR#101");
                Logger.getLogger(DrugEditor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
}//GEN-LAST:event_btnDeleteActionPerformed

