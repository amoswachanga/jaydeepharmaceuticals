    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String query;
        if(modify){
            query = "UPDATE drugs SET `name` = ?,`desc` = ?, `code` = ?, `price` =? WHERE did="+druglist.get(index).getDid() +";";
        }else{
            query = "INSERT INTO drugs (`name`, `desc`, `code`, `price`) VALUES ( ?, ?, ?, ?);";
        }
        try{
            double value = Double.parseDouble(txtprice.getText());
            try {
                PreparedStatement pstmt = (PreparedStatement) db.con.prepareStatement(query);
                pstmt.setString(1,txtname.getText());
                pstmt.setString(2,txtdesc.getText());
                pstmt.setDouble(4,value);
                pstmt.setString(3,txtdrug_no.getText());
                
                pstmt.executeUpdate();
                initDrugsList();
                lbl_info.setText("Success in inserting the values.");
                edit(false);
            } catch (SQLException ex) {
                lbl_info.setText("Error editing or inserting the values.");
                Logger.getLogger(DrugEditor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }catch(NumberFormatException e){
            Logger.getLogger(DrugEditor.class.getName()).log(Level.SEVERE, null, e);
        }
}//GEN-LAST:event_btnSaveActionPerformed

